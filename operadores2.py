# Operadores Booleanos
print("Operadores Booleanos")
# < <=
print(10 < 10)
print(10 <= 10)
# > >=
print(10 > 10)
print(10 >= 10)
#==
print(10 == 10)
#!=
print(10 != 10) 

# Operadores Lógicos
print("Operadores Lógicos")
# and (True if all true)
print(True and True)
print(True and False)
# or (True if at least one True)
print(False or False)
print(False or True)
# not True
print(not True)
print(not False)